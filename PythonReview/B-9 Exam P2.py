# Exam Practice 2
# 求 1-2+3-4+5...99 的计算结果

# 1. 留意到，表达式奇数项的系数为 (+1)，偶数项系数为 (-1)【不考虑从 0 开始】
# 2. 项数从 range(1, 100)，共 99 项

sum = 0
for i in range(1, 100):
    # 系数由奇偶项控制
    if i % 2 == 0:
        sum -= i
    else:
        sum += i

print(sum)

# 【另一种思路】
